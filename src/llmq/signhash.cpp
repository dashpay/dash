#include <llmq/signhash.h>

#include <hash.h>
#include <serialize.h>

#include <vector>

namespace llmq
{

SignHash::SignHash(Consensus::LLMQType llmqType, const uint256& quorumHash, const uint256& id, const uint256& msgHash)
{
    CHashWriter h(SER_GETHASH, 0);
    h << llmqType;
    h << quorumHash;
    h << id;
    h << msgHash;
    m_hash = h.GetHash();
}

} // namespace llmq